    {
        return array_merge(
            $this->wfaBoys(),
            $this->wfaGirls(),
            $this->lhfaBoys(),
            $this->lhfaGirls(),
            $this->hcfaBoys(),
            $this->hcfaGirls(),
            $this->bfaBoys(),
            $this->bfaGirls(),
        );
    }

