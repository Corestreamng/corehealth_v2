/**
 * WHO Child Growth Standards (0–60 months)
 *
 * Data source: WHO Multicentre Growth Reference Study Group (2006)
 * "WHO Child Growth Standards: Length/height-for-age, weight-for-age,
 *  weight-for-length, weight-for-height and body mass index-for-age:
 *  Methods and development."
 *
 * Reference: https://www.who.int/tools/child-growth-standards/standards
 *
 * Indicators seeded:
 *   wfa  — Weight-for-age (0–60 months)
 *   lhfa — Length/Height-for-age (0–60 months)
 *   hcfa — Head circumference-for-age (0–60 months)
 *   bfa  — BMI-for-age (0–60 months)
 *
 * LMS parameters: L (Box-Cox power), M (median), S (coefficient of variation)
 * Z-score formula:
 *   L ≠ 0:  z = [(y/M)^L − 1] / (L × S)
 *   L = 0:  z = ln(y/M) / S
 */
class WhoGrowthStandardsSeeder extends Seeder
{
    public function run()
    {
        // Truncate for idempotent re-runs
        DB::table('who_growth_standards')->truncate();

        $now = now();
        $rows = [];

        // Build all rows from data arrays
        foreach ($this->getAllData() as $entry) {
            [$indicator, $sex, $month, $L, $M, $S] = $entry;
            $rows[] = [
                'indicator'  => $indicator,
                'sex'        => $sex,
                'age_months' => $month,
                'l_value'    => $L,
                'm_value'    => $M,
                's_value'    => $S,
                'created_at' => $now,
                'updated_at' => $now,
            ];
        }

        // Insert in chunks of 100
        foreach (array_chunk($rows, 100) as $chunk) {
            DB::table('who_growth_standards')->insert($chunk);
        }

        $this->command->info('WHO Growth Standards seeded: ' . count($rows) . ' rows (' . count($rows) / 2 . ' per sex).');
    }

    /**
     * Returns all WHO LMS data as flat array of [indicator, sex, month, L, M, S].
     */
